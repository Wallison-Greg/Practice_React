/*Manipulando a "Dom" via js:
Basicamente para termos acesso ao corpo da aplicação "html" e nescessario link o script dentro do proprio html, feito isso para que o javascript tenha acesso a as tags e nescessario utilizar o operador "document" atravez dele que podemos coletar dados do html ou gerar novas tags */

//Ex: alterando o valor da tag "title"

document.title = 'JavaScript - Dom';

//para formatar uma tag utilizando as tacks css via js e nescessario chamar o operador "style" e referencia o tipo de formatação mais o valor que essa formatação ira receber 

document.body.style.backgroundColor = 'orange'; //observa-se que foi passado tanto o operador "style" para a tag como o tipo de formatação que seria aplicado


