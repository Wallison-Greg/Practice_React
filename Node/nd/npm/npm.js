/*Basicamente utilizamos o NPM para varias funcionalidades, entre elas ele e bastante utilizado para baixar pacotes esxternos como "babel, webpack, express..."

para utilizarmos o "NPM" e nescessario instalarmos o pacote dele, para isso basta utilizar o seguinte codigo 

code: npm init -y

basicamente com isso ele ira gerar o arquivo "package.json" 

com o NPM instalado podemos instalar os demais pacotes utilizando o seguinte codigo 

code: npm i "nome do pacote"

basicamente utilizamos apenas o valor "npm i" e passar o nome do pacote que deseja ser intalado ele ira baixar a verção mais atualizada 

caso qeuira atualizar algum pacote intalado basta utilizar o seguinte codigo 

code: npm update 

caso queira desinstalar algum pacote em expecifico basta utilizar o seguinte codigo 

code: npm uninstall "nome do pacote"*/